package jul29;

public class File {
    private String file1 ;
    private String file2 ;
    private String file3 ;

    public File(String file1, String file2, String file3) {
        this.file1 = file1;
        this.file2 = file2;
        this.file3 = file3;
    }

    public String getFile1() {
        return file1;
    }


    public void setFile2(String file2) {
        this.file2 = file2;
    }

    public String getFile3() {
        return file3;
    }

    public void setFile3(String file3) {
        this.file3 = file3;
    }
}
